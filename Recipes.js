        const recipeModal = document.getElementById('recipeModal');
        const closeModal = document.getElementById('closeModal');
        const modalTitle = document.getElementById('modalRecipeTitle');
        const modalSubtitle = document.getElementById('modalRecipeSubtitle');
        const modalImage = document.getElementById('modalRecipeImage');
        const modalIngredients = document.getElementById('modalIngredients');
        const modalInstructions = document.getElementById('modalInstructions');
        const prepTimeElement = document.getElementById('prepTime');
        const difficultyElement = document.getElementById('difficultyLevel');
        const viewRecipeButtons = document.querySelectorAll('.view-recipe-btn');

        // Mobile Menu Elements
        const mobileMenu = document.getElementById('mobile-menu');
        const navMenu = document.querySelector('.nav-menu');

        // Function to open recipe modal
        function openRecipe(recipeKey) {
            const recipe = recipes[recipeKey];
            
            if (recipe) {
                // Set modal content
                modalTitle.textContent = recipe.title;
                modalSubtitle.textContent = recipe.subtitle;
                prepTimeElement.textContent = recipe.prepTime;
                difficultyElement.textContent = recipe.difficulty;
                
                // Set modal image - try primary image first, then fallback
                modalImage.src = recipe.image;
                modalImage.alt = recipe.title;
                
                // Clear previous content
                modalIngredients.innerHTML = '';
                modalInstructions.innerHTML = '';
                
                // Add ingredients
                recipe.ingredients.forEach(ingredient => {
                    const li = document.createElement('li');
                    li.textContent = ingredient;
                    modalIngredients.appendChild(li);
                });
                
                // Add instructions
                recipe.instructions.forEach((step, index) => {
                    const li = document.createElement('li');
                    li.textContent = step;
                    modalInstructions.appendChild(li);
                });
                
                // Show modal with animation
                recipeModal.classList.add('active');
                document.body.style.overflow = 'hidden';
            }
        }

        // Function to close recipe modal
        function closeRecipeModal() {
            recipeModal.classList.remove('active');
            setTimeout(() => {
                document.body.style.overflow = 'auto';
            }, 300);
        }

        // Event Listeners for View Recipe buttons
        viewRecipeButtons.forEach(button => {
            button.addEventListener('click', (e) => {
                e.preventDefault();
                const recipeKey = button.getAttribute('data-recipe');
                openRecipe(recipeKey);
            });
        });

        // Close modal when clicking X
        closeModal.addEventListener('click', closeRecipeModal);

        // Close modal when clicking outside
        recipeModal.addEventListener('click', (e) => {
            if (e.target === recipeModal) {
                closeRecipeModal();
            }
        });

        // Close modal with Escape key
        document.addEventListener('keydown', (e) => {
            if (e.key === 'Escape' && recipeModal.classList.contains('active')) {
                closeRecipeModal();
            }
        });

        // Mobile menu toggle
        mobileMenu.addEventListener('click', function() {
            mobileMenu.classList.toggle('active');
            navMenu.classList.toggle('active');
        });

        // Close mobile menu when clicking a link
        const navLinks = document.querySelectorAll('.nav-link');
        navLinks.forEach(link => {
            link.addEventListener('click', () => {
                mobileMenu.classList.remove('active');
                navMenu.classList.remove('active');
            });
        });

        // Close mobile menu when clicking outside
        document.addEventListener('click', (e) => {
            if (!e.target.closest('.nav-container') && navMenu.classList.contains('active')) {
                mobileMenu.classList.remove('active');
                navMenu.classList.remove('active');
            }
        });

        // Image loading error handling for all images
        document.querySelectorAll('img').forEach(img => {
            img.addEventListener('error', function() {
                // If image fails to load, show a placeholder with gradient
                this.style.background = 'linear-gradient(135deg, #3AA9D6, #6DBB45)';
                this.style.display = 'flex';
                this.style.alignItems = 'center';
                this.style.justifyContent = 'center';
                this.style.color = 'white';
                this.style.fontWeight = 'bold';
                this.style.padding = '20px';
                this.style.textAlign = 'center';
                
                // Try alternative image name if available
                const altSrc = this.getAttribute('alt').toLowerCase().replace(/\s+/g, '_');
                const currentSrc = this.src;
                
                // If image is from recipes modal, try to find alternative
                if (currentSrc.includes('/image/')) {
                    // Try different extensions
                    const basePath = currentSrc.substring(0, currentSrc.lastIndexOf('.'));
                    const extensions = ['.png', '.jpg', '.jpeg', '.gif'];
                    
                    let tryCount = 0;
                    function tryNextExtension() {
                        if (tryCount < extensions.length) {
                            this.src = basePath + extensions[tryCount];
                            tryCount++;
                        } else {
                            // All extensions failed, show placeholder
                            this.innerHTML = '<div style="color:white; font-weight:bold; text-align:center;">Recipe<br>Image</div>';
                        }
                    }
                    
                    // Bind tryNextExtension to this img
                    const tryExtension = tryNextExtension.bind(this);
                    tryExtension();
                } else {
                    this.innerHTML = '<div style="color:white; font-weight:bold; text-align:center;">Recipe<br>Image</div>';
                }
            });
        });

